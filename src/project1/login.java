    public boolean validations()
    {
        String username= txtuser.getText();
        String password=new String(txtpass.getPassword());
        if(username.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Enter the Valid username!!");
            return false;
        }
        if(password.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Enter the Valid password!!");
            return false;
        }
        return true;
    }
    public void getdetails()
    {
        String username= txtuser.getText();
        String password=new String(txtpass.getPassword());
         try
        {
          Connection con = commonfunction.getconnection();
          String query="select * from login where firstname = '"+username+"' and pwd = '"+password+"' ";
          Statement st=con.createStatement();
          ResultSet rs;
           rs=st.executeQuery(query);
          if(rs.next())
          {
              JOptionPane.showMessageDialog(this, "login Sucessfully!!");
             
          }
          else
          {
              JOptionPane.showMessageDialog(this, "Invalid Credentials!");
          }
          txtuser.setText("");
          txtuser.requestFocus();
          txtpass.setText("");
          
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }
